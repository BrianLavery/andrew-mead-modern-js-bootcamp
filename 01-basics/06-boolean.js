// === equality operator
let temp = 31;
let isFreezing = temp === 32; // false

console.log(isFreezing);

// !== inequality operator
isFreezing = temp !== 32; // true
console.log(isFreezing);

// < less than operator
isFreezing = temp < 32; // true
console.log(isFreezing);

// > greater than operator
isFreezing = temp > 32; // false
console.log(isFreezing);

